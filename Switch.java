public class Switch {
    public static void main(String[] arg){
        int val = 5;
        switch(val){
            case 3:{
                System.out.println("val is 3");
                break;
            }
            case 2:{
                System.out.println("val is 2");
                break;
            }
            default:{
                System.out.println("val is none of these");
                break;
            }
        }
    }
}
